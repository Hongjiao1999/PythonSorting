array = [61, 65, 46, 51, 23, 18, 48, 45, 13, 21, 86, 49, 84, 32, 1]


def 冒泡排序(要排序的数组):
	for i in range(1, len(要排序的数组)):
		for j in range(0, len(要排序的数组) - 1):
			if 要排序的数组[j] > 要排序的数组[j + 1]:
				要排序的数组[j], 要排序的数组[j + 1] = 要排序的数组[j + 1], 要排序的数组[j]
		#   if 要排序的数组[j] > 要排序的数组[j + 1]:
		#       要排序的数组[j], 要排序的数组[j + 1] = 要排序的数组[j + 1], 要排序的数组[j]
	'''
	j > j +1 表示升序排列，j +1 > j 表示降序排列
	'''
	return 要排序的数组


print(冒泡排序(array))


